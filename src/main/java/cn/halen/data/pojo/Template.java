package cn.halen.data.pojo;

public class Template {
	private int id;
	
	private String name;
	
	private String logistics_type;
	
	private String area;
	
	private int start_standard;
	
	private int start_fee;
	
	private int add_standard;
	
	private int add_fee;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLogistics_type() {
		return logistics_type;
	}

	public void setLogistics_type(String logistics_type) {
		this.logistics_type = logistics_type;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public int getStart_standard() {
		return start_standard;
	}

	public void setStart_standard(int start_standard) {
		this.start_standard = start_standard;
	}

	public int getStart_fee() {
		return start_fee;
	}

	public void setStart_fee(int start_fee) {
		this.start_fee = start_fee;
	}

	public int getAdd_standard() {
		return add_standard;
	}

	public void setAdd_standard(int add_standard) {
		this.add_standard = add_standard;
	}

	public int getAdd_fee() {
		return add_fee;
	}

	public void setAdd_fee(int add_fee) {
		this.add_fee = add_fee;
	}
	
}
